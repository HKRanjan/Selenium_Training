public class caseStatement_2 {

   public static void main (String args[]){

int i = 40;

switch (i){
   case 20:
      System.out.println("Size = Small");
      break;
   case 25:
      System.out.println("Size = Medium");
      break;
   case 40:
      System.out.println("Size = Large");
      break;
   default:
      System.out.println("Size = Not Defined");
}

   }

}